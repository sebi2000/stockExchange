package Offer;

public class Offer {
    public int id;
    public int price;
    public String name;
    public int units;
    public String type;
    public String user;

    public Offer(int id, int price, String name, int units, String user,String type) {
        this.id = id;
        this.price = price;
        this.name = name;
        this.units = units;
        this.user = user;
        this.type=type;
    }




}
